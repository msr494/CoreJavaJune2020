package JavaBasics;

public class Name_and_address {// this is another way which is alternative of Camel case

	public static void main(String[] args) {

		System.out.println("Mohammad Sharkar");
		System.out.print("625w 57th st"); // print vs println
		System.out.println("NY 10019"); // println print line by line
		// use of \n, next line
		System.out.println(
				"I have a question. \nIn Java, are there clear rules on when to use each of access modifiers, namely the default (package private), public, protected and private, while making class and interface and dealing with inheritance? \nAns: I don't know.");
		// use of \t, tab
		System.out.println("\nMy name is John, \tI am a CA, \tLive in TX");
		// use of \r, carriage return
		System.out.println("Mohammad Sharkar, 3476537214");
		System.out.println("\rMohammad Sharkar, \r3476537214");
		System.out.println("\n\rMohammad Sharkar, \r3476537214");
		// TODO I have to dig down \r
		// String concatenation
		System.out.println("Mohammad" + " Sharkar" + ", 45");
		System.out.println("\"Tofael\"");
		System.out.println("\'Tofael\'");
		System.out.println("Tofael");

		
	}

}
