package hw7Q4IfElseCondition02;


public class Weather {
public static void main(String[] args) {
	int temp = -8;
	
	if (temp >0 ) {
		System.out.println("The temp is positive");
	} else if (temp <0) {
		System.out.println("the temp is negative");
		
	} else {
		System.out.println("the temp is zero");
		
	}
	
	if (temp % 2 == 0 ) {
		
		System.out.println("the temp is even");
	} else {
		System.out.println("the temp is odd");
	}
	
	
	
	
	
}
}
